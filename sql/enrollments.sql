CREATE TABLE IF NOT EXISTS enrollments (
    id SERIAL PRIMARY KEY,
    reference_number VARCHAR(20) NOT NULL UNIQUE,
    school_year VARCHAR(20) NOT NULL,
    enrollment_type VARCHAR(20) NOT NULL,
    grade_level VARCHAR(10) NOT NULL,
    strand VARCHAR(50) DEFAULT NULL,
    tvl_specialization VARCHAR(50) DEFAULT NULL,
    lrn VARCHAR(20) DEFAULT NULL,
    psa_birth_cert VARCHAR(20) DEFAULT NULL,
    last_name VARCHAR(100) NOT NULL,
    first_name VARCHAR(100) NOT NULL,
    middle_name VARCHAR(100) DEFAULT NULL,
    extension_name VARCHAR(20) DEFAULT NULL,
    email VARCHAR(255) DEFAULT NULL,
    birthdate DATE NOT NULL,
    place_of_birth_city VARCHAR(100) NOT NULL,
    place_of_birth_province VARCHAR(100) DEFAULT NULL,
    mother_tongue VARCHAR(50) DEFAULT NULL,
    sex VARCHAR(10) NOT NULL,
    with_lrn VARCHAR(5) DEFAULT 'Yes',
    returning_learner VARCHAR(5) DEFAULT 'No',
    ip_community VARCHAR(5) DEFAULT 'No',
    ip_specify VARCHAR(100) DEFAULT NULL,
    four_ps_beneficiary VARCHAR(5) DEFAULT 'No',
    four_ps_household_id VARCHAR(50) DEFAULT NULL,
    disability VARCHAR(5) DEFAULT 'No',
    disability_type JSONB DEFAULT NULL,
    current_address VARCHAR(255) DEFAULT NULL,
    current_city VARCHAR(100) DEFAULT 'Cebu City',
    current_province VARCHAR(100) DEFAULT 'Cebu',
    current_barangay VARCHAR(100) DEFAULT NULL,
    current_zip_code VARCHAR(10) DEFAULT '6000',
    current_country VARCHAR(50) DEFAULT 'Philippines',
    same_address VARCHAR(5) DEFAULT 'Yes',
    permanent_address VARCHAR(255) DEFAULT NULL,
    permanent_city VARCHAR(100) DEFAULT NULL,
    permanent_province VARCHAR(100) DEFAULT NULL,
    permanent_barangay VARCHAR(100) DEFAULT NULL,
    permanent_zip_code VARCHAR(10) DEFAULT NULL,
    permanent_country VARCHAR(50) DEFAULT NULL,
    father_name VARCHAR(200) DEFAULT NULL,
    father_contact VARCHAR(20) DEFAULT NULL,
    mother_maiden_name VARCHAR(200) DEFAULT NULL,
    mother_contact VARCHAR(20) DEFAULT NULL,
    guardian_name VARCHAR(200) DEFAULT NULL,
    guardian_contact VARCHAR(20) DEFAULT NULL,
    status VARCHAR(20) DEFAULT 'Pending',
    checked_requirements JSONB DEFAULT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
